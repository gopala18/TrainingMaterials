DROP TABLE BOOK_TRANSACTION;
DROP TABLE BOOKS;
DROP TABLE AUTHORS;
DROP TABLE MEMBERS;
DROP TABLE PUBLISHERS;


CREATE   TABLE   AUTHORS
(
AUTHOR_ID     INT   CONSTRAINT AUTHORS_ID_PK PRIMARY KEY,
AUTHOR_NAME   VARCHAR(45)
);

CREATE   TABLE  MEMBERS
(
MEMBER_ID     INT   CONSTRAINT MEMBERS_ID_PK PRIMARY KEY,
MEMBER_NAME   VARCHAR(45),
MEMBER_DEPT   VARCHAR(30),
MEMBER_ST_DA  DATE,
STATUS	      VARCHAR(1)
);

CREATE  TABLE  PUBLISHERS
(
PUBLISHER_ID     INT CONSTRAINT PUBLISHERS_ID_PK PRIMARY KEY,
PUBLISHER_NAME   VARCHAR(45),
PUBLISHER_ADDR   VARCHAR(35)
);

CREATE    TABLE  BOOKS
(
BOOK_ID	      INT CONSTRAINT BOOKS_ID_PK PRIMARY KEY,
TITLE	      VARCHAR(30),
BOOK_CATEGORY VARCHAR(10),
COPY_NO       INT,
AUTHOR_ID     INT,
PUBLISHER_ID  INT, 
EDITION       INT,
YE_OF_PUB     INT,
PRICE	      INT,
RACK_NUMBER   INT,
CONSTRAINT BOOKS_AUTHORID_FK FOREIGN KEY(AUTHOR_ID) REFERENCES AUTHORS(AUTHOR_ID),
CONSTRAINT BOOKS_PUBID_FK FOREIGN KEY(PUBLISHER_ID) REFERENCES PUBLISHERS(PUBLISHER_ID)
);


CREATE   TABLE   BOOK_TRANSACTION
(
BOOK_ID	       INT,
MEMBER_ID      INT, 
ISSUE_DATE     DATE,
DUE_DATE       DATE,
RETURN_DATE    DATE,
CONSTRAINT  BOOK_TRANS_ID_PK PRIMARY KEY(BOOK_ID,MEMBER_ID,ISSUE_DATE),
CONSTRAINT BOOK_TRAN_BOOKID_FK FOREIGN KEY(BOOK_ID) REFERENCES BOOKS(BOOK_ID),
CONSTRAINT BOOK_TRANS_MEMID_FK FOREIGN KEY(MEMBER_ID) REFERENCES MEMBERS(MEMBER_ID)
);

